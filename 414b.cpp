ll dp[2001][2001];
ll help(ll n, ll k, ll i, ll prev){
    if(i==k)return 1;
    if(prev>=n)return 1;
    ll ans=0;
    if(dp[i][prev]!=-1)return dp[i][prev];
    for(int j=prev;j<=n;j+=prev){
        ans+=help(n,k,i+1,j);
        ans%=mod;
    }
    return dp[i][prev]=ans%mod;
}

void solution(){
    ll n,m;
    cin>>n;
    ll k;
    cin>>k;
    memset(dp,0,sizeof(dp));
    ll ans=0;
    for(int i=0;i<=n;i++){
        for(int j=0;j<=n;j++){
            if(i==j)dp[i][j]=1;
        }
        // dp[0][i]=1;
        dp[i][0]=1;
    }
    // ans=help(n,k,0,1);
    for(int i=1;i<=n;i++){
        for(int j=1;j<=k;j++){
            for(int k=1;k<=n;k+=j){
                ans+=dp[i-1][k];
            }
        }
    }
    ans%=mod;
    pn(ans);
    return;
}

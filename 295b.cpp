void help(vector<vector<int>>v, vector<int>&q){
    // floyd warshall
    int n=v.size();
    for(int k=0;k<n;k++){
        for(int i=0;i<n;i++){
            for(int j=0;j<n;j++){
                v[i][j]=min(v[i][j],v[i][k]+v[k][j]);
            }
        }
        for(int i=0;i<=k;i++){
            for(int j=0;j<=k;j++){
                q[n-k-1]+=v[i][j];
            }
        }
    }
    // reverse(q.begin(),q.end());
    printv(q);
}


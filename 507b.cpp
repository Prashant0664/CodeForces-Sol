#include<bits/stdc++.h>
using namespace std;
double r,a,b,c,d;

int main(){
    cin>>r>>a>>b>>c>>d;
    double dis=sqrt((a-c)*(a-c)+(b-d)*(b-d))/2;
    int g=dis/r;
    dis-=r*g;
    if(dis>0)g++;
    cout<<g;
}
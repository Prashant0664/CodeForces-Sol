ll check(ll n, ll k, ll mid){
    ll left=((k+mid)*(k+mid+1))/2-(k*(k-1))/2;
    ll right=((k+n)*(k+n-1))/2-left-(k*(k-1))/2;
    // cout<<left<<" "<<right<<":\n";
    return abs(right-left);
}

void solution(){
    ll n,m;
    cin>>n;
    // cin>>m;
    ll k;
    cin>>k;
    if(n==2){
        cout<<abs(k-k+1);
        endo;
        return;
    }
    ll low=1;
    ll high=n;
    while(low<=high){
        ll mid=(low+high)>>1;
        ll mid2=mid+1;
        ll x=check(n,k,mid);
        ll x2=check(n,k,mid2);
        if(x>x2){
            low=mid+1;
        }
        else{
            high=mid-1;
        }
        // cout<<mid<<" "<<x<<endl;
    }
    pn(check(n,k,low));
    return;
}

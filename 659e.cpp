void dfs(vector<vector<int>>&adj, vector<int>&vis, int i, int &ans, int parent=-1){
    if(vis[i]==1 || ans==1){
        ans=1;
        return;
    }
    vis[i]=1;
    for(auto &k:adj[i]){
        if(k!=parent){
            dfs(adj, vis, k, ans, i);
        }
    }
    return;
}
void solution(){
    ll n,m;
    cin>>n;
    cin>>m;
    vector<vector<int>>v(n);
    for(int i=0;i<m;i++){
        ll a,b;
        cin>>a>>b;
        a--,b--;
        v[a].push_back(b);
        v[b].push_back(a);
    }
    int res=0;
    vector<int>vis(n,0);
    for(int i=0;i<n;i++){
        if(vis[i]==0){
            int ans=0;
            dfs(v, vis, i, ans);
            if(!ans)res++;
        }
    }
    pn(res);
    return;
}
